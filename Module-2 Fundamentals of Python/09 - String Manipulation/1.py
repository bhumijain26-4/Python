"""
Write a Python program to demonstrate string slicing.
"""
str = "Python Programming"
print("str[0:5]",str[0:5]) # for starting slicing
print("str[0:10:12]",str[0:10:2]) # for middle slicing
print("str[-6:]",str[-6:]) # for last slicing
print("str[-6:-1]",str[-6:-1]) # for backwards slicing
print("str[::-1]",str[::-1]) # for reverse string
